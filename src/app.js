const specialSymbols = ['[', ']', '\\', '^', '$', '.', '|', '?', '*', '+', '(', ')'];
inputArray.forEach(el =>
    console.log(el, ' comma(,): ', checkInput(el, ','), ' dot(.): ', checkInput(el, '.')));

function checkInput(input, separator) {
    if (specialSymbols.includes(separator)) {
        console.log('includes', separator);
        separator = '\\' + separator;
    }
    const regex = new RegExp(`^-?(\\d+(${separator}\\d+)?|\\d+(${separator}\\d+)?(e|E)(\\+|-)?\\d+)$`);
    return regex.test(input);
}

